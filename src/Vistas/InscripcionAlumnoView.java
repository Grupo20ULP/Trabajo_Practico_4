    private void jBtnInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnInscribirActionPerformed
        // TODO add your handling code here:
        
        Alumno alumnoSelect =(Alumno) jcbAlumno.getSelectedItem();
        Materia materiaSelect= (Materia) jcbMateria.getSelectedItem();
        if (alumnoSelect != null && materiaSelect != null){
            alumnoSelect.agregarMateria(materiaSelect);
        }else{
                JOptionPane.showConfirmDialog(this, "No existen alumnos o materias.",
                "ERROR_MESSAGE", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jBtnInscribirActionPerformed

