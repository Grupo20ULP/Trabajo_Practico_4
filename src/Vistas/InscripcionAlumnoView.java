    private void cargaMaterias(){
        jcbMateria.removeAllItems();
        if(lMaterias != null){
        for(Materia materia: lMaterias){
            jcbMateria.addItem(materia);
        }}
    }
    private void cargaAlumno(){
        if(lAlumnos != null){
        for(Alumno alumno: lAlumnos){
            jcbAlumno.addItem(alumno);
        }}
    }

